let currentLanguage = 'en';

function setElementTranslation(el, text) {
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
        return;
    }

    if (el.tagName === 'OPTION') {
        el.textContent = text;
        return;
    }

    if (el.hasAttribute('data-i18n-html')) {
        return;
    }

    el.textContent = text;
}

function applyTranslations() {
    const lang = translations[currentLanguage];
    if (!lang) return;

    document.documentElement.lang = currentLanguage === 'te' ? 'te' : 'en';
    document.body.classList.toggle('lang-te', currentLanguage === 'te');
    document.title = lang.page_title;

    const langLabel = document.getElementById('currentLang');
    if (langLabel) {
        langLabel.textContent = currentLanguage === 'en' ? 'తెలుగు' : 'English';
    }

    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.getAttribute('data-i18n');
        if (key && lang[key] !== undefined) {
            setElementTranslation(el, lang[key]);
        }
    });

    document.querySelectorAll('[data-i18n-html]').forEach((el) => {
        const key = el.getAttribute('data-i18n-html');
        if (key && lang[key] !== undefined) {
            el.innerHTML = lang[key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key && lang[key] !== undefined) {
            el.placeholder = lang[key];
        }
    });

    document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
        const key = el.getAttribute('data-i18n-alt');
        if (key && lang[key] !== undefined) {
            el.alt = lang[key];
        }
    });

    if (typeof renderFileList === 'function') {
        renderFileList();
    }
}

