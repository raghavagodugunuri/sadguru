function initCarousel() {
    const slides = document.querySelectorAll('.carousel-slide');
    const indicators = document.querySelectorAll('.indicator');
    const prevBtn = document.querySelector('.carousel-btn.prev');
    const nextBtn = document.querySelector('.carousel-btn.next');
    if (!slides.length) return;

    let currentSlide = 0;

    function showSlide(index) {
        slides.forEach((slide) => slide.classList.remove('active'));
        indicators.forEach((ind) => ind.classList.remove('active'));
        currentSlide = (index + slides.length) % slides.length;
        slides[currentSlide].classList.add('active');
        if (indicators[currentSlide]) {
            indicators[currentSlide].classList.add('active');
        }
    }

    if (prevBtn) {
        prevBtn.setAttribute('type', 'button');
        prevBtn.addEventListener('click', () => showSlide(currentSlide - 1));
    }

    if (nextBtn) {
        nextBtn.setAttribute('type', 'button');
        nextBtn.addEventListener('click', () => showSlide(currentSlide + 1));
    }

    indicators.forEach((ind) => {
        ind.addEventListener('click', () => showSlide(parseInt(ind.dataset.slide, 10)));
    });

    setInterval(() => showSlide(currentSlide + 1), 6000);
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initCarousel);
} else {
    initCarousel();
}

// ─── Admin & File Library (simple backend) ───
const ADMIN_PASSCODE = 'sadhguru2024';
const SESSION_KEY = 'templeAdminSession';
const API_BASE = '';

async function apiListResources() {
    const res = await fetch(`${API_BASE}/api/files`);
    if (!res.ok) throw new Error('list_failed');
    return await res.json();
}

async function apiUploadResource({ title, description, fileType, file }) {
    const form = new FormData();
    form.append('title', title);
    form.append('description', description);
    form.append('fileType', fileType);
    form.append('file', file);

    const res = await fetch(`${API_BASE}/api/files`, {
        method: 'POST',
        body: form
    });
    if (!res.ok) throw new Error('upload_failed');
    return await res.json();
}

async function apiDeleteResource(id) {
    const res = await fetch(`${API_BASE}/api/files/${encodeURIComponent(id)}`, { method: 'DELETE' });
    if (!res.ok) throw new Error('delete_failed');
    return await res.json();
}

function isAdminLoggedIn() {
    return sessionStorage.getItem(SESSION_KEY) === 'true';
}

function setAdminSession(loggedIn) {
    if (loggedIn) {
        sessionStorage.setItem(SESSION_KEY, 'true');
    } else {
        sessionStorage.removeItem(SESSION_KEY);
    }
}

function t(key) {
    return translations[currentLanguage][key] || translations.en[key] || key;
}

function getFileIcon(fileType, mimeType) {
    const map = {
        pdf: 'fa-file-pdf',
        audio: 'fa-file-audio',
        video: 'fa-file-video',
        image: 'fa-file-image',
        document: 'fa-file-word'
    };
    if (map[fileType]) return map[fileType];
    if (mimeType && mimeType.startsWith('image/')) return 'fa-file-image';
    if (mimeType && mimeType.startsWith('audio/')) return 'fa-file-audio';
    if (mimeType && mimeType.startsWith('video/')) return 'fa-file-video';
    return 'fa-file';
}

function showToast(message, isError) {
    const toast = document.getElementById('uploadToast');
    if (!toast) return;
    toast.textContent = message;
    toast.classList.toggle('error', !!isError);
    toast.classList.add('show');
    setTimeout(() => toast.classList.remove('show'), 3000);
}

function updateAdminUI() {
    const loggedIn = isAdminLoggedIn();
    const uploadCard = document.getElementById('uploadCard');
    const adminBar = document.getElementById('adminBar');
    const secretBtn = document.getElementById('secretAdminBtn');

    if (uploadCard) uploadCard.classList.toggle('locked', !loggedIn);
    if (adminBar) adminBar.classList.toggle('active', loggedIn);
    if (secretBtn) {
        secretBtn.classList.toggle('admin-active', loggedIn);
        secretBtn.innerHTML = loggedIn
            ? '<i class="fas fa-unlock"></i>'
            : '<i class="fas fa-lock"></i>';
    }
}

function renderFileList() {
    const fileList = document.getElementById('fileList');
    const emptyMsg = document.getElementById('fileListEmpty');
    if (!fileList) return;

    apiListResources().then((resources) => {
        resources = Array.isArray(resources) ? resources : [];
        resources.sort((a, b) => new Date(b.uploadedAt) - new Date(a.uploadedAt));

        fileList.querySelectorAll('li:not(#fileListEmpty)').forEach((el) => el.remove());

        if (resources.length === 0) {
            if (emptyMsg) emptyMsg.style.display = '';
            return;
        }

        if (emptyMsg) emptyMsg.style.display = 'none';

        const loggedIn = isAdminLoggedIn();

        resources.forEach((res) => {
            const li = document.createElement('li');
            const mimeType = res.mimeType || '';
            const fileType = res.fileType || '';
            li.innerHTML = `
                <div class="file-name">
                    <i class="fas ${getFileIcon(fileType, mimeType)} file-icon"></i>
                    <span>${res.title || ''}</span>
                </div>
                <div>
                    <button type="button" class="download-btn" data-id="${res.id}">
                        <span data-i18n="download_btn">${t('download_btn')}</span>
                    </button>
                    ${loggedIn ? `<button type="button" class="admin-delete-btn" data-delete="${res.id}">${t('admin_delete')}</button>` : ''}
                </div>
            `;
            fileList.appendChild(li);
        });

        fileList.querySelectorAll('.download-btn').forEach((btn) => {
            btn.addEventListener('click', () => downloadFile(btn.dataset.id));
        });

        fileList.querySelectorAll('.admin-delete-btn').forEach((btn) => {
            btn.addEventListener('click', () => {
                if (confirm('Delete this resource?')) {
                    apiDeleteResource(btn.dataset.delete)
                        .then(() => renderFileList())
                        .catch(() => showToast(t('upload_error'), true));
                }
            });
        });
    }).catch(() => {
        // Backend not running or blocked
        fileList.querySelectorAll('li:not(#fileListEmpty)').forEach((el) => el.remove());
        if (emptyMsg) emptyMsg.style.display = '';
        showToast('Backend is not running. Start the server to see uploads.', true);
    });
}

function downloadFile(id) {
    const link = document.createElement('a');
    link.href = `${API_BASE}/api/files/${encodeURIComponent(id)}/download`;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
}

function handleUpload() {
    if (!isAdminLoggedIn()) {
        showToast(t('upload_locked_msg'), true);
        return;
    }

    const title = document.getElementById('file-title').value.trim();
    const description = document.getElementById('file-description').value.trim();
    const fileInput = document.getElementById('file-upload');
    const fileType = document.getElementById('file-type').value;
    const file = fileInput.files[0];

    if (!title || !file) {
        showToast(t('upload_error'), true);
        return;
    }

    apiUploadResource({ title, description, fileType, file }).then(() => {
        document.getElementById('file-title').value = '';
        document.getElementById('file-description').value = '';
        fileInput.value = '';
        showToast(t('upload_success'));
        renderFileList();
    }).catch(() => showToast(t('upload_error'), true));
}

function openAdminModal() {
    const modal = document.getElementById('adminModal');
    const error = document.getElementById('adminLoginError');
    const passcode = document.getElementById('adminPasscode');

    if (isAdminLoggedIn()) {
        document.getElementById('library').scrollIntoView({ behavior: 'smooth' });
        return;
    }

    if (modal) modal.classList.add('active');
    if (error) error.classList.remove('show');
    if (passcode) {
        passcode.value = '';
        passcode.focus();
    }
}

function closeAdminModal() {
    const modal = document.getElementById('adminModal');
    if (modal) modal.classList.remove('active');
}

function handleAdminLogin() {
    const passcode = document.getElementById('adminPasscode').value;
    const error = document.getElementById('adminLoginError');

    if (passcode === ADMIN_PASSCODE) {
        setAdminSession(true);
        updateAdminUI();
        closeAdminModal();
        showToast(t('admin_logged_in'));
        document.getElementById('library').scrollIntoView({ behavior: 'smooth' });
    } else if (error) {
        error.classList.add('show');
    }
}

function handleAdminLogout() {
    setAdminSession(false);
    updateAdminUI();
    renderFileList();
    showToast(t('admin_logout'));
}

function initAdminSystem() {
    const secretBtn = document.getElementById('secretAdminBtn');
    const modalClose = document.getElementById('adminModalClose');
    const loginBtn = document.getElementById('adminLoginBtn');
    const logoutBtn = document.getElementById('adminLogoutBtn');
    const uploadBtn = document.getElementById('uploadSubmitBtn');
    const passcodeInput = document.getElementById('adminPasscode');
    const modal = document.getElementById('adminModal');

    if (secretBtn) secretBtn.addEventListener('click', openAdminModal);
    if (modalClose) modalClose.addEventListener('click', closeAdminModal);
    if (loginBtn) loginBtn.addEventListener('click', handleAdminLogin);
    if (logoutBtn) logoutBtn.addEventListener('click', handleAdminLogout);
    if (uploadBtn) uploadBtn.addEventListener('click', handleUpload);

    if (passcodeInput) {
        passcodeInput.addEventListener('keydown', (e) => {
            if (e.key === 'Enter') handleAdminLogin();
        });
    }

    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) closeAdminModal();
        });
    }

    updateAdminUI();
    renderFileList();
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initAdminSystem);
} else {
    initAdminSystem();
}
